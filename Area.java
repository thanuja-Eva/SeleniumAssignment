public class Area {
	

	public static double areaOfSquare(int side) {
		double area=side*side;
		return area;
		
	}
	
	public  static double areaOfTraphezium(double a, double b,double height) {
		double trapheziumArea=((a+b)*height)/2;
		return trapheziumArea;
	}
	

}
